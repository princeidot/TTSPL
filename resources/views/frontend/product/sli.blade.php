                                <h3 class="pbmit-title">Sonar Level Interface Principle of Operation</h3>
                                <p class="mt-3">The HAWK Sultan Sonar uses Advanced Sonar Technology to measure and
                                    control Waste Water Clarifiers and Thickeners.
                                    The system is easy to use and the innovative design provides critical plant control
                                    to optimize performance.<br>
                                    In the water, wastewater industry process conditions will vary greatly between a
                                    primary sedimentation tank, secondary / final
                                    clarifier, and a gravity thickener. Thickener bed levels, secondary RAS blanket,
                                    flocculent blanket, etc, all have different densities and
                                    the water above these interface levels are subject to different process conditions
                                    that change.
                                </p>
                                <p>
                                    <b>To optimize performance in each interface application under all process
                                        environments:</b><br>
                                    HAWK has developed a powerful low-frequency transducer to penetrate through the
                                    suspended solids and is capable of measuring the
                                    sludge bed level at the bottom of a clarifier or the lightest flocculent layer.
                                </p>
                                <p><b>To optimize performance under all process environments in each interface
                                        application:</b><br>
                                    HAWK uses one transducer with a frequency and power level that is applicable to the
                                    density of the interface and process conditions
                                    expected in the tank. HAWK can guarantee performance for controlling pumps etc,
                                    rather than for monitoring purposes only.</p>

