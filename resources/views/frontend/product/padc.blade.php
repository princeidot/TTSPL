                            <h3 class="mb-3"> Principle of Operation</h3>
                            <p>The Praetorian system interrogator unit is connected to one end of a fiber optic cable
                                which is attached to or buried with the cable or
                                infrastructure being monitored. The interrogator produces rapidly pulsed laser light set
                                at a precise frequency that excites the fiber and
                                causes it to be responsive to physical changes around it. Some of this light is
                                reflected back (backscattered) to the light source where
                                the interrogator records and analyses looking for changes to its color relating to
                                physical effects in the application.</p>


                            <h3 class="mb-3"> Functionality of Power and Data Cable Monitoring</h3>
                            <p>Praetorian Fiber Optic Sensing (FOS) uses a combination of Distributed Acoustic Sensing
                                (DAS) and Distributed Temperature
                                Sensing (DTS) to protect underground buried assets. By exciting a fiber optic core
                                within a cable the Praetorian Interrogator is able to
                                utilize the fibers as a distributed network equivalent to up to 1.6 Million individual
                                vibration, temperature, and strain sensors.<br>
                                Because a praetorian utilizes a number of different sensing methods it is possible to
                                observe events in a number of physically
                                independent ways, consequently Praetorian is inherently resistant to taking a given
                                reading and giving a false alarm due to the
                                requirement for multiple physical effects to simultaneously occur at the same location
                                to signify an event and trigger an alarm.</p>
