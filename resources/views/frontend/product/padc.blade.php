                                    inspection.<br>
                                    Monitoring for wear, damage, or corrosion of the cable is extremely difficult and
                                    often power failure or data outage is the first sign of
                                    a problem.<br>
                                    Often, these assets are installed in areas that have multiple uses by various stakeholders and in the event of a failure or damage
                                    can be logistically difficult and expensive to access for repair.<br>
                                    Due to their accessibility when compared to overhead lines, it should also be noted
                                    that third-party intrusion (both accidental
                                    and nefarious) is a much greater threat to buried power transmission lines.
                                    Subsequently the risk of exposure to Arc Flash from live
                                    conductors and the potential for electric shock or electrocution are much greater. As
                                    well as the possibility for optical cables to be
                                    damaged leading to extended telecommunication outages.<br>
                                    Additionally performance of these cables is constantly degrading over time due to
                                    material and environmental factors. This effect
                                    although slow and unpredictable is inevitable and ultimately causes failure of the
                                    conductor on the optic. The location of these faults is a
                                    major challenge and a significant contributor to downtime.<br>
                                    Ultimately to manage these risks a method of around-the-clock monitoring is required
                                    that can be fitted during construction or
                                    retrofitted to existing infrastructure. Due to the long runs transmission cables
                                    need to cover, traditional instrumentation must be installed at regular intervals along the cable run and require supporting
                                    infrastructure (power and communications) to each
                                    device, an entirely impractical proposal.

