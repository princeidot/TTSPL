                                        <h3>Principle of Operation</h3>
                                        <p>Guided-wave technology sends the radar pulse down a probe to measure either
                                            liquids, solids, or a low dielectric to high dielectric
                                            Interface level. <br>
                                            The pulse hits the surface and / or Interface and is reflected back up the
                                            probe to the sensor, where the transit time is translated into
                                            a distance using time of fight and time expansion. The amplitude of the
                                            reflection depends on the dielectric constant of the product Centurion Guided Radar.</p>

