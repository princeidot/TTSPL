                                    approximately 100 pulses per second. If the path between the Sender and Receiver is
                                    blocked by any object or material which
                                    absorbs or reflects microwave energy the Receiving unit will no longer detect the
                                    complete transmission chain and indicate via Relay
                                    for automatic indication and process control requirements.
                                </p>
