                        <div class="candidate-detail-description">
                            <h3 class="mb-3"> Measuring principle</h3>
                            <p>A radar signal is emitted via an antenna, reflected from the product surface and received
                                after a
                                time t. The radar principle used is FMCW (Frequency Modulated Continuous Wave).<br>
                                The FMCW-radar transmits a high frequency signal whose frequency increases linearly
                                during
                                the measurement phase (called the frequency sweep). The signal is emitted, reflected on
                                the
                                measuring surface and received with a time delay, t. Delay time, t=2d/c, where d is the
                                distance
                                to the product surface and c is the speed of light in the gas above the product.<br>
                                For further signal processing the difference Δf is calculated from the actual
                                transmitted
                                frequency and the received frequency. The difference is directly proportional to the
                                distance. A
                                large frequency difference corresponds to a large distance and vice versa. The frequency
                                difference Δf is transformed via a Fast Fourier Transform (FFT) into a frequency
                                spectrum and
                                then the distance is calculated from the spectrum. The level results from the difference
                                between
                                the tank height and the measured distance.</p>
                        </div>

